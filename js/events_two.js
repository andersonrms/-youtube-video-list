function changevideo02() {
  divMain = document.getElementById("main-header");
  document.getElementById("temp").remove();
  div = document.createElement("div");
  div.setAttribute("id", "temp");
  frm = document.createElement("IFRAME");
  frm.setAttribute("src", "https://www.youtube.com/embed/Tw9Bbw0wdbI");
  frm.setAttribute("frameborder", "0");
  frm.setAttribute("allowfullscreen", "");
  frm.setAttribute("class", "thumb-main");
  div.appendChild(frm);
  divMain.appendChild(div);
  document.getElementById("txt1").innerHTML = "Por dentro da ABBR";
}

